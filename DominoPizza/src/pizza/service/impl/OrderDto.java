package pizza.service.impl;

public class OrderDto {

	private String id;
	private String sa_pro;
	private String menu_kind;
	private String menu_qty;
	private String menu_price;
	private String menu_name;
	//GetterSetter
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSa_pro() {
		return sa_pro;
	}
	public void setSa_pro(String sa_pro) {
		this.sa_pro = sa_pro;
	}
	public String getMenu_kind() {
		return menu_kind;
	}
	public void setMenu_kind(String menu_kind) {
		this.menu_kind = menu_kind;
	}
	public String getMenu_qty() {
		return menu_qty;
	}
	public void setMenu_qty(String menu_qty) {
		this.menu_qty = menu_qty;
	}
	public String getMenu_price() {
		return menu_price;
	}
	public void setMenu_price(String menu_price) {
		this.menu_price = menu_price;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	
	
	
}
